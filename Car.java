public class Car
{
    // make and manufacturer strings
    public String make;
    public String manufacturer;
    // bool for handicap spaces
    public boolean isHandicap;
    
    // constructor
    public Car(String make, String manufacturer, boolean isHandicap)
    {
        this.manufacturer = manufacturer;
        this.make = make;
        this.isHandicap = isHandicap;
    }

    
}